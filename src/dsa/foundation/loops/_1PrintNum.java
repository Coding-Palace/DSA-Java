package dsa.foundation.loops;

public class _1PrintNum {
	public static void main(String[] args) {
		/**
		 * using while
		 */
		int i = 1;
		while (i <= 9) {
			System.out.println(i);
			i = i + 1;
		}

		/**
		 * using for
		 */
		for (int j = 1; j <= 9; j++) {
			System.out.println(j);

		}
	}
}
